    private void Undomethod(){
        if(st.isEmpty()){
            JOptionPane.showMessageDialog(this,"Please enter value first","Undo",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
              JButton toBeDeletedBtn=st.pop().btn;
              boolean flag=false;
              Stack<Pair> tempst=new Stack<Pair>();
              while(!st.isEmpty()){
                  Pair p=st.pop();
                  tempst.push(p);
                  JButton tempbtn=p.btn;
                  String prevalue=p.val;
                  if(toBeDeletedBtn==tempbtn){
                      flag=true;
                      tempbtn.setText(prevalue);
                      break;
                  }
              }
                  if(flag==false){
                      toBeDeletedBtn.setText("");
                      toBeDeletedBtn.setBackground(white);    
                  }
                  while(!tempst.isEmpty()){
                      Pair p=tempst.pop();
                      st.push(p);
                  }
              }
    }
